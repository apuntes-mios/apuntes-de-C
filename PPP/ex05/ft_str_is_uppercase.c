int		ft_str_is_uppercase(char *str)
{
	int l;

	l = 0;
	if(str[l] == '\0')
		return 1;
	while(str[l])
	{
		if(str[l] < 'A' || str[l] > 'Z')
			return 0;
		l++ ;
	}
	return 1;
}
