#include<stdio.h>
int		ft_str_is_alpha(char *str)
{
	int l;

	l = 0;	
	if(str[l] == '\0')
		return 1;
	while(str[l])
	{
		if((str[l] < 'A' || str[l] > 'Z') && (str[l] < 'a' || str[l] > 'z'))
			return 0;
		l++ ;
	}
	return 1;
}
