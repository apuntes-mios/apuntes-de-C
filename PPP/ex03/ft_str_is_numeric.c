int		ft_str_is_numeric(char *str)
{
	int l;

	l = 0;
	if(str[l] == '\0')
		return 1;
	while(str[l])
	{
		if(str[l] < '0' || str[l] > '9')
			return 0;
		l++ ;
	}
	return 1;
}
