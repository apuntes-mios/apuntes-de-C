void	ft_rev_int_tab(int *tab, int size)
{
	int v[size];
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < size)
	{
		v[i] = tab[i];
		i++;
	}
	i--;
	while (i >= 0)
	{
		tab[j] = v[i];
		i--;
		j++;
	}
}
