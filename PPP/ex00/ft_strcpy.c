char	*ft_strcpy(char *dest, char *src)
{
	char	*s;
	int		i;

	s = src;
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (s);
}
